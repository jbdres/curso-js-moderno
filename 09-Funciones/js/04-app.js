/** La Diferencia entre Función y Método */
// Nota: Basicamente la función y el método son practicamente la misma cosa ya que ambas son reutilizables

const numero1 = 20;
const numero2 = "20";

// Esta es una función. Estructura: nombre + parentesis
console.log(parseInt(numero2));

// Este es un método. Estructura: objeto + . + nombre + parentesis
console.log(numero1.toString());